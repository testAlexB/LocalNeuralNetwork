# =============== 5.5 Unicode normalization ===============
add_heading("5.1 Нормализация Unicode")

add_para(
    "Один и тот же символ можно записать несколькими способами в Unicode. "
    "Например, буква «Ё» может быть одним токеном U+0401 (Ё как есть) "
    "или составным: «Е» (U+0415) + диакритика ¨ (U+0308). "
    "В UTF-8 это будут разные последовательности байт, а значит — "
    "разные токены. Модель будет думать, что «Ё» и «Ё» — разные вещи."
)

add_para(
    "Стандарт Unicode определяет формы нормализации:"
)

add_bullet(
    "NFC (Normalization Form C) — составные символы там, где возможно. "
    "«Ё» → один код U+0401. Рекомендован W3C для веба."
)
add_bullet(
    "NFD (Normalization Form D) — разложение на базовый символ + диакритику. "
    "«Ё» → «Е» + «¨». Используется в системах поиска."
)
add_bullet(
    "NFKC / NFKD — дополнительно заменяют совместимые символы "
    "(например, ² → 2, ﬁ → fi). Полезны для очистки текста."
)

add_para(
    "Для корпуса FishingLLM достаточно применить NFC (самый простой "
    "и стандартный вариант). Если этого не сделать, редкие диакритические "
    "варианты букв раздуют словарь бесполезными дубликатами."
)

add_code_block(
    'import unicodedata\n\n'
    'text = "\\u0401"  # Ё как один символ\n'
    'text2 = "\\u0415\\u0308"  # Е + ¨\n'
    'print(unicodedata.normalize("NFC", text2))  # Ё\n\n'
    '# Применение ко всему корпусу\n'
    'normalized = unicodedata.normalize("NFC", raw_text)'
)

