add_heading("7.1 Реализация через HuggingFace tokenizers", level=2)

add_para(
    "Библиотека tokenizers от HuggingFace предоставляет готовый "
    "BPE-тренажёр с Rust-бэкендом для скорости:"
)

add_code_block(
    'from tokenizers import Tokenizer\n'
    'from tokenizers.models import BPE\n'
    'from tokenizers.trainers import BpeTrainer\n'
    'from tokenizers.pre_tokenizers import ByteLevel\n'
    'from tokenizers.decoders import ByteLevel as ByteLevelDecoder\n\n'
    '# Создаём BPE-токенизатор\n'
    'tokenizer = Tokenizer(BPE(unk_token="<unk>"))\n\n'
    '# Явно задаём разделение на слова (GPT-2 паттерн)\n'
    'tokenizer.pre_tokenizer = ByteLevel(add_prefix_space=False)\n\n'
    '# Настройка тренировки\n'
    'trainer = BpeTrainer(\n'
    '    vocab_size=16_000,\n'
    '    special_tokens=[\n'
    '        "<unk>", "<s>", "</s>",\n'
    '        "<|user|>", "<|assistant|>", "<|system|>"\n'
    '    ],\n'
    '    min_frequency=2,  # минимальная частота пары для слияния\n'
    ')\n\n'
    '# Тренировка\n'
    'files = ["fishing_corpus.txt"]\n'
    'tokenizer.train(files, trainer)\n\n'
    '# Пробуем\n'
    'output = tokenizer.encode("Щука на спиннинг")\n'
    'print(output.tokens)  # например: ["щу", "ка", "_", "на", ...]\n'
    'print(output.ids)     # [341, 890, 307, 56, ...]\n\n'
    '# Декодирование обратно в текст\n'
    'tokenizer.decoder = ByteLevelDecoder()\n'
    'print(tokenizer.decode(output.ids))  # "Щука на спиннинг"'
)

