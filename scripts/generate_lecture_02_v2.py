# =============== 8. Zipf ===============
add_heading("8. Закон Ципфа")

add_para(
    "George Zipf (1935) обнаружил: в любом тексте частота слова "
    "обратно пропорциональна его рангу. f(r) ~ 1/r, где r — ранг "
    "(1 — самое частое слово). Иными словами: 10% самых частых слов "
    "покрывают ~80% текста."
)

add_para(
    "Для токенов распределение тоже подчиняется Ципфу. Эмпирическая "
    "оценка: в словаре 16K токенов первые ~1000 токенов будут "
    "покрывать 80-90% текста (цифра зависит от корпуса и алгоритма "
    "токенизации). Остальные 15K — редкие подслова для специфической "
    "лексики («верховодка», «мормышка», «фидер»)."
)

add_para(
    "Практическое следствие: при обучении модели gradient "
    "будет доминировать от частых токенов. Редкие токены "
    "обновляются реже. Это одна из причин, почему нужно "
    "много данных — чтобы редкие токены тоже получили "
    "достаточно обновлений."
)

